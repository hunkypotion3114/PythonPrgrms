CREATE TABLE BOOKS (
    Book_Id VARCHAR(5),
    Title VARCHAR(30) UNIQUE,
    Author VARCHAR(20) NOT NULL,
    Type VARCHAR(15)  ,
    Publisher VARCHAR(20) DEFAULT 'BPB',
    Quantity INTEGER(10),
    Price INTEGER(10),
    DateOfPurchase DATE,
    PRIMARY KEY (Book_Id)   
);

